def fetch_prayer_times(city: str) -> dict | None:
    try:
        url = f"https://api.aladhan.com/v1/timingsByCity?city={city}&country=SA&method=4"
        resp = requests.get(url, timeout=10)
        data = resp.json()
        if data.get("code") == 200:
            timings = data["data"]["timings"]
            return {k: timings[k][:5] for k in PRAYER_NAMES_ORDER if k in timings}
    except Exception:
        pass
    return None

def get_prayer_times_for_location(location_key: str) -> str:
    info = SAUDI_REGIONS.get(location_key) or SAUDI_CITIES.get(location_key)
    if not info:
        return "لم أجد المدينة المطلوبة 🤍"
    city_en = info["city"]
    city_ar = info["name"]
    times = fetch_prayer_times(city_en)
    if not times:
        return f"تعذّر جلب أوقات الصلاة لـ {city_ar} حالياً 🤍"
    lines = [f"🕌 أوقات الصلاة — {city_ar}\n"]
    for k in PRAYER_NAMES_ORDER:
        lines.append(f"{_PRAYER_AR.get(k, k)} — {times.get(k, '—')}")
    return "\n".join(lines)

def get_prayer_times_text() -> str:
    times = fetch_prayer_times("Makkah")
    if not times:
        return "تعذّر جلب أوقات الصلاة حالياً 🤍"
    lines = ["🕌 أوقات الصلاة — مكة المكرمة\n"]
    for k in PRAYER_NAMES_ORDER:
        lines.append(f"{_PRAYER_AR.get(k, k)} — {times.get(k, '—')}")
    return "\n".join(lines)

def get_single_prayer_text(prayer_key: str) -> str:
    times = fetch_prayer_times("Makkah")
    if not times or prayer_key not in times:
        return f"حان وقت {_PRAYER_AR.get(prayer_key, prayer_key)} 🕌\nحي على الصلاة 🤍"
    return f"حان وقت {_PRAYER_AR.get(prayer_key, prayer_key)} — {times[prayer_key]} 🕌\nحي على الصلاة 🤍"

