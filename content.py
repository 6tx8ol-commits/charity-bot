def to_12h(time_str):
    h, m = map(int, time_str.split(":"))
    period = "ص" if h < 12 else "م"
    h12 = h % 12
    if h12 == 0:
        h12 = 12
    return f"{h12}:{m:02d} {period}"

def fetch_prayer_times(city):
    import requests
    try:
        r = requests.get(
            f"http://api.aladhan.com/v1/timingsByCity?city={city}&country=Saudi%20Arabia&method=4",
            timeout=10
        )
        data = r.json()["data"]["timings"]
        return {p: data[p] for p in PRAYER_NAMES_ORDER}
    except:
        return None

def get_prayer_times_text():
    return "اختر المنطقة او المدينة لعرض اوقات الصلاة 🤍"

def get_prayer_times_for_location(location_key):
    all_locations = {**SAUDI_REGIONS, **SAUDI_CITIES}
    if location_key not in all_locations:
        return "موقع غير معروف"

    loc = all_locations[location_key]
    times = fetch_prayer_times(loc["city"])
    if not times:
        return f"تعذر جلب اوقات الصلاة لـ {loc['name']} حاليا — حاول مرة اخرى 🤍"

    text = f"اوقات الصلاة — {loc['name']} 🤍\n"
    for p in PRAYER_NAMES_ORDER:
        info = PRAYER_VIRTUES[p]
        text += f"\n═══ {info['name']} — {to_12h(times[p])} ═══\n{info['virtue']}\n"

    text += "\nحافظوا على الصلاة في وقتها 🤍"
    return text

def get_single_prayer_text(prayer_key):
    makkah = fetch_prayer_times("Makkah")
    riyadh = fetch_prayer_times("Riyadh")
    info = PRAYER_VIRTUES[prayer_key]

    text = f"حان وقت صلاة {info['name']} 🤍\n"
    if makkah:
        text += f"\nمكة: {to_12h(makkah[prayer_key])}"
    if riyadh:
        text += f"\nالرياض: {to_12h(riyadh[prayer_key])}"
    text += f"\n\n{info['virtue']}"
    text += "\n\nلا تضيعوا الصلاة — حافظوا عليها في وقتها 🤍"
    return text


